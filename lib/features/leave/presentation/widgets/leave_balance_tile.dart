import 'package:flutter/material.dart';
import 'package:lms/features/leave/data/models/leave_balance_model.dart';
import '../utils/leave_color_mapper.dart';

class LeaveBalanceTile extends StatelessWidget {
  final LeaveBalance balance;

  const LeaveBalanceTile({super.key, required this.balance});

  @override
  Widget build(BuildContext context) {
    final name = balance.name;
    final available = balance.available;
    final carried = balance.carried;
    final reserved = balance.pendingReserved;

    final Color accentColor = LeaveColorMapper.colorFor(name);

    return Container(
      margin: const EdgeInsets.only(bottom: 14),
      padding: const EdgeInsets.all(18),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(18),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withOpacity(0.06),
            blurRadius: 14,
            offset: const Offset(0, 8),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          /// Header
          Row(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Expanded(
                child: Text(
                  name,
                  style: const TextStyle(
                    fontSize: 16,
                    fontWeight: FontWeight.w600,
                  ),
                ),
              ),
              Column(
                crossAxisAlignment: CrossAxisAlignment.end,
                children: [
                  Text(
                    available.toStringAsFixed(0),
                    style: TextStyle(
                      fontSize: 28,
                      fontWeight: FontWeight.bold,
                      color: accentColor,
                    ),
                  ),
                  const Text(
                    'days available',
                    style: TextStyle(fontSize: 12, color: Colors.black54),
                  ),
                ],
              ),
            ],
          ),

          if (carried > 0 || reserved > 0) ...[
            const SizedBox(height: 14),
            const Divider(height: 1),
            const SizedBox(height: 12),

            Row(
              children: [
                if (carried > 0)
                  _MetaText(
                    label: 'Carried',
                    value: carried,
                    color: accentColor,
                  ),
                if (reserved > 0) ...[
                  const SizedBox(width: 16),
                  _MetaText(
                    label: 'Reserved',
                    value: reserved,
                    color: Colors.orange,
                  ),
                ],
              ],
            ),
          ],
        ],
      ),
    );
  }
}

class _MetaText extends StatelessWidget {
  final String label;
  final double value;
  final Color color;

  const _MetaText({
    required this.label,
    required this.value,
    required this.color,
  });

  @override
  Widget build(BuildContext context) {
    return RichText(
      text: TextSpan(
        style: const TextStyle(fontSize: 13),
        children: [
          TextSpan(
            text: '$label: ',
            style: const TextStyle(color: Colors.black54),
          ),
          TextSpan(
            text: value.toStringAsFixed(0),
            style: TextStyle(fontWeight: FontWeight.w600, color: color),
          ),
        ],
      ),
    );
  }
}
